    private void table_typetransportasiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_typetransportasiMouseClicked
        // TODO add your handling code here:
        try {
            ResultSet resu = conn.createStatement().executeQuery("SELECT * FROM "+tablename+" WHERE id_transportasi="
            +table_typetransportasi.getValueAt(table_typetransportasi.getSelectedRow(), 0).toString());
            if (resu.next()) {
                selected=resu.getInt("id_transportasi");
                txt_kode.setText(resu.getString("kode"));
                txt_jumlahkursi.setText(resu.getString("jumlah_kursi"));
                txt_keterangan.setText(resu.getString("keterangan"));
                combo_typetransportasi.setSelectedIndex(arrayid.indexOf(resu.getInt("id_type_transportasi"))+1);
                enableButton(true);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_table_typetransportasiMouseClicked

