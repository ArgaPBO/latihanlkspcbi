    public void showCombo() {
        arrayid = new ArrayList<Integer>();
        Object[] obj = {
            " "
        };
        combomodel = new DefaultComboBoxModel(obj);
        combo_typetransportasi.setModel(combomodel);
        try {
            String query = "SELECT id_type_transportasi,nama_type FROM type_transportasi";
            ResultSet resu = conn.createStatement().executeQuery(query);
            while (resu.next()) {
                arrayid.add(resu.getInt("id_type_transportasi"));
                combomodel.addElement(resu.getString("nama_type"));
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    public void showData() {
        tabmodel.getDataVector().removeAllElements();
        tabmodel.fireTableDataChanged();
        try {
            ResultSet resu = conn.createStatement().executeQuery("SELECT * FROM "+tablename);
            while (resu.next()) {
                String tipe = null;
                ResultSet res1 = conn.createStatement().executeQuery("SELECT nama_type FROM type_transportasi WHERE id_type_transportasi="+resu.getString("id_type_transportasi"));
                if (res1.next()) {
                    tipe = res1.getString("nama_type");
                }
                Object[] data = {
                    resu.getInt("id_transportasi"),
                    resu.getString("kode"),
                    resu.getString("jumlah_kursi"),
                    resu.getString("keterangan"),
                    tipe
                };
                tabmodel.addRow(data);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e);
        }
        
    }
    
    public void enableButton(Boolean a) {
        if (a==true) {
            btn_Update.setText(updatetext+" ("+selected+")");
            btn_Delete.setText(deletetext+" ("+selected+")");
        } else if (a==false) {
            btn_Update.setText(updatetext);
            btn_Delete.setText(deletetext);
        }
        btn_Update.setEnabled(a);
        btn_Delete.setEnabled(a);
    }
    
    
    
